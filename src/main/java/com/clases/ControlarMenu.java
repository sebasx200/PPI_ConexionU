package com.clases;

public class ControlarMenu {

    private double nuevoAncho;
    private double nuevoAlto;

    public ControlarMenu(double nuevoAncho, double nuevoAlto) {
        this.nuevoAncho = nuevoAncho;
        this.nuevoAlto = nuevoAlto;
    }
    public double getNuevoAncho() {
        return nuevoAncho;
    }
    public double getNuevoAlto() {
        return nuevoAlto;
    }
}
